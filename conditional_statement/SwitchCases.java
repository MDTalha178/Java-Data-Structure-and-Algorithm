public class SwitchCases {
    public static void main(String args[]){
        System.out.println("Lets test Switch");

        int a = 2;
        switch (a) {
            case 1:
                System.out.println("Your are great!");
                break;
            case 2:
                System.out.println("Your are best!");
                break;
            case 3:
                System.out.println("Your are  fool!");
                break;
            default:
                System.out.println("Your are nothing");
        }
    }
}
